        <div class="modal-content">
            <div class="modal-header">
                <h5 class="modal-title" id="modalTitle">Tambah Jenis Tugas</h5>
                <button type="button" class="close" data-dismiss="modal" aria-label="Close">
                    <span aria-hidden="true">&times;</span>
                </button>
            </div>
            <div class="modal-body">
                <div class="form-group">
                    <label>Nama Jenis Tugas</label>
                    <input type="text" name="jenis_nama" id="jenis_nama" class="form-control" required>
                    <small id="error-jenis_nama" class="error-text form-text text-danger"></small>
                </div>
            </div>
            <div class="modal-footer">
